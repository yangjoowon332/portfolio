function translatePage(lang) {
    document.querySelectorAll("[data-i18n]").forEach(el => {
      const key = el.getAttribute("data-i18n");
      if (I18N[lang] && I18N[lang][key]) {
        // innerHTML로 넣으면 <br>도 그대로 유지됨
        el.innerHTML = I18N[lang][key];
      }
    });
    document.documentElement.lang = (lang === "jp") ? "jp" : "ko";
  }
  
  const btn = document.getElementById("krjpLangBtn");
  let current = "ko"; // 기본은 한국어
  btn.addEventListener("click", () => {
    current = (current === "ko") ? "jp" : "ko";
    translatePage(current);
    btn.textContent = (current === "ko") ? "🇯🇵" : "🇰🇷";
  });
  
  // 초기화
  translatePage(current);
  // 처음엔 한국어 페이지니까 버튼에는 jp(🇯🇵) 보이게
  btn.textContent = "🇯🇵";